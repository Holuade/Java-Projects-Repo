    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        
        try {
         Connection conn = ConnectionProvider.getCon();
         Statement stm = conn.createStatement();
         ResultSet rst = stm.executeQuery("select * from addstaff where staffid = '" + id + "'");
         DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel(); 
         dtm.setRowCount(0);
         
         while(rst.next()){
             Object obj[] ={rst.getInt("StaffID"),rst.getString("Name"),rst.getString("Post"),
             rst.getString("ContactNo"),rst.getString("DOB"),rst.getString("Gender"),rst.getString("Address"),
             rst.getString("Status")};
             
             dtm.addRow(obj);
         }
        
         if(!rst.first()){
             jLabel3.setVisible(true); 
         }
         else{
             jTextField1.setEditable(false);
              flag = 1;
         }
      }
      catch(Exception e){
         JOptionPane.showMessageDialog(null, e); 
      }
    }//GEN-LAST:event_jButton1ActionPerformed

